    func populateRSARandomTitles() {
        
        let realm = RLMRealm.defaultRealm()
        let nouns = RSANoun.allObjects()
        let adjectives = RSAAdjective.allObjects()
        
        println(nouns.count)
        println(adjectives.count)
        
        if nouns.count == 0 && adjectives.count == 0 {
            populateData()
            realm.beginWriteTransaction()
            for n in nounArray {
                let newNoun = RSANoun()
                newNoun.name = n as! String
                realm.addObject(newNoun)
            }
            for a in adjectiveArray {
                let newAdjective = RSAAdjective()
                newAdjective.name = a as! String
                realm.addObject(newAdjective)
            }
            realm.commitWriteTransaction()
            realm.writeCopyToPath("/Users/markcheng/Desktop/words.realm", error: nil)
        }
    }

